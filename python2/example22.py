# Set variables
temperature = 35
is_raining = True

# Conditional statements with logical operators
if temperature > 30 and is_raining:
    print("It's hot and raining outside, take an umbrella!")
elif temperature > 30:
    print("It's hot outside, wear light clothes!")
elif is_raining:
    print("It's raining outside, take an umbrella!")
else:
    print("The weather is pleasant.")
